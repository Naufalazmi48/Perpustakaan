    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        if (!invalid() && callback == null) {
            bookInf.create(getResult());
            bookInf.read(tb_book);
            clearUI();
        } else if (callback != null) {
            JOptionPane.showMessageDialog(null, "Data ini telah tersedia");
            clearUI();
        } else {
            JOptionPane.showMessageDialog(null, "Isi semua data terlebih dahulu");
        }
    }//GEN-LAST:event_btn_saveActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        if (!invalid()) {
            if (bookInf.delete(callback.getId())) {
                callback = null;
                bookInf.read(tb_book);
            }
                clearUI();
        } else {
            JOptionPane.showMessageDialog(null, "Pilih data terlebih dahulu");
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        if (!invalid()) {
            if (bookInf.update(getResult(), callback.getId())) {
                callback = null;
                bookInf.read(tb_book);
            }
                clearUI();
        } else {
            JOptionPane.showMessageDialog(null, "Pilih data terlebih dahulu");
        }
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        if (!tf_searchbook.getText().isEmpty()) {
            try {
                bookInf.search(tb_book, Integer.valueOf(tf_searchbook.getText()));
            } catch (Exception e) {
                bookInf.search(tb_book, tf_searchbook.getText());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Masukan nim di dalam kolom pencarian");
        }
    }//GEN-LAST:event_btn_searchActionPerformed

    private void tf_searchbookKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_searchbookKeyReleased
        if (tf_searchbook.getText().isEmpty()) {
            bookInf.read(tb_book);
        }
    }//GEN-LAST:event_tf_searchbookKeyReleased

    private void tb_bookMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_bookMouseClicked
        int row = tb_book.getSelectedRow();
        Book user = new Book();
        user.setTitle((String) tb_book.getValueAt(row, 0));
        user.setAuthor((String) tb_book.getValueAt(row, 1));
        user.setYear((int) tb_book.getValueAt(row, 2));
        user.setPublisher((String) tb_book.getValueAt(row, 3));
        user.setStock((int) tb_book.getValueAt(row, 4));
        user.setId((int) tb_book.getValueAt(row, 5));
        updateUI(user);
        callback = user;
    }//GEN-LAST:event_tb_bookMouseClicked

    private void tf_totalKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_totalKeyTyped
        char mChar = evt.getKeyChar();
        if (!(Character.isDigit(mChar))) {
            evt.consume();
        }
    }//GEN-LAST:event_tf_totalKeyTyped

    private void tf_yearKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_yearKeyTyped
        char mChar = evt.getKeyChar();
        if (!(Character.isDigit(mChar))) {
            evt.consume();
        }
        if(tf_year.getText().length() >3){
            evt.consume();
        }
    }//GEN-LAST:event_tf_yearKeyTyped

