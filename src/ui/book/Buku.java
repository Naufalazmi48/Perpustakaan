    private Book getResult() {
        Book book = new Book();
        book.setTitle(tf_title.getText());
        book.setAuthor(tf_author.getText());
        book.setPublisher(tf_publish.getText());
        book.setYear(Integer.valueOf(tf_year.getText()));
        book.setStock(Integer.valueOf(tf_total.getText()));
        return book;
    }

    private void updateUI(Book book) {
        tf_author.setText(book.getAuthor());
        tf_publish.setText(book.getPublisher());
        tf_year.setText(String.valueOf(book.getYear()));
        tf_total.setText(String.valueOf(book.getStock()));
        tf_title.setText(book.getTitle());
    }

    private void clearUI() {
        tf_author.setText("");
        tf_publish.setText("");
        tf_searchbook.setText("");
        tf_title.setText("");
        tf_total.setText("");
        tf_year.setText("");
    }

    private boolean invalid() {
        return tf_author.getText().isEmpty() || tf_publish.getText().isEmpty() || tf_title.getText().isEmpty()
                || tf_total.getText().isEmpty() || tf_year.getText().isEmpty();
    }

